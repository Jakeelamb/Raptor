#!/usr/bin/env python3

from __future__ import annotations

import csv
from pathlib import Path


ROOT = Path(__file__).resolve().parent
RESULTS_DIR = ROOT / "results"
CSV_PATH = ROOT / "benchmark_summary.csv"
MD_PATH = ROOT / "benchmark_summary.md"


def read_kv_file(path: Path) -> dict[str, str]:
    data: dict[str, str] = {}
    if not path.exists():
        return data

    for line in path.read_text().splitlines():
        parts = line.split("\t", 1)
        if len(parts) == 2:
            data[parts[0]] = parts[1]
    return data


def read_text(path: Path) -> str:
    if not path.exists():
        return "N/A"
    return path.read_text().strip() or "N/A"


def collect_rows() -> list[dict[str, str]]:
    rows: list[dict[str, str]] = []
    if not RESULTS_DIR.exists():
        return rows

    for run_dir in sorted(p for p in RESULTS_DIR.iterdir() if p.is_dir()):
        dataset = run_dir.name.rsplit("_", 2)[0] if "_" in run_dir.name else run_dir.name

        for tool in ("raptor", "spades"):
            tool_dir = run_dir / tool
            if not tool_dir.exists():
                continue

            stats = read_kv_file(tool_dir / "stats.txt")
            rows.append(
                {
                    "run": run_dir.name,
                    "dataset": dataset,
                    "tool": tool,
                    "time_seconds": read_text(tool_dir / "time.txt"),
                    "peak_memory_kb": read_text(tool_dir / "memory.txt"),
                    "num_contigs": stats.get("num_contigs", "N/A"),
                    "total_length": stats.get("total_length", "N/A"),
                    "largest": stats.get("largest", "N/A"),
                    "n50": stats.get("n50", "N/A"),
                    "contigs_1k": stats.get("contigs_1k", "N/A"),
                    "contigs_10k": stats.get("contigs_10k", "N/A"),
                    "contigs_100k": stats.get("contigs_100k", "N/A"),
                }
            )
    return rows


def write_csv(rows: list[dict[str, str]]) -> None:
    fieldnames = [
        "run",
        "dataset",
        "tool",
        "time_seconds",
        "peak_memory_kb",
        "num_contigs",
        "total_length",
        "largest",
        "n50",
        "contigs_1k",
        "contigs_10k",
        "contigs_100k",
    ]

    with CSV_PATH.open("w", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)


def write_markdown(rows: list[dict[str, str]]) -> None:
    lines = [
        "# Benchmark Summary",
        "",
        "This file is generated by `summarize_results.py` from the local `results/` directory.",
        "",
        "| Run | Dataset | Tool | Time (s) | Peak RSS (KB) | Contigs | Total Length | Largest | N50 |",
        "|-----|---------|------|----------|---------------|---------|--------------|---------|-----|",
    ]

    for row in rows:
        lines.append(
            "| {run} | {dataset} | {tool} | {time_seconds} | {peak_memory_kb} | "
            "{num_contigs} | {total_length} | {largest} | {n50} |".format(**row)
        )

    if not rows:
        lines.append("| - | - | - | - | - | - | - | - | - |")

    MD_PATH.write_text("\n".join(lines) + "\n")


def main() -> None:
    rows = collect_rows()
    write_csv(rows)
    write_markdown(rows)
    print(f"Wrote {len(rows)} rows to {CSV_PATH}")
    print(f"Wrote markdown summary to {MD_PATH}")


if __name__ == "__main__":
    main()
